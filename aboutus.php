 <div style="text-align: justify;">Save the Children is a global non-profit organisation which was founded in the year 1919. Today, it is India's leading independent child rights NGO. As of December&nbsp;2018, we work in 19&nbsp;states of India. Started in 2008 in India, and registered as 'Bal Raksha Bharat', we have&nbsp;changed the lives of more than 10.1 million (1.1 crore) children till date. In the year 2018, we reached 12.03&nbsp;lakh children.<br>
<br>
Save the Children believes that every child deserves the best chance for a bright future and that’s why we are fiercely committed towards ensuring that children not only survive, but thrive. We run programmes in the remotest corners of India and urban areas to provide quality education and healthcare, protection from harm and abuse and life-saving aid during emergencies to children. Globally, Save the Children is present in more than 80 countries and works to improve the lives of the most vulnerable children living there.<br>
